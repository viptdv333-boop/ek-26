const RECENT_KEY = 'ek26_recent_emoji';

function getRecent(): string[] {
  try {
    return JSON.parse(localStorage.getItem(RECENT_KEY) || '[]').slice(0, 32);
  } catch { return []; }
}

function addRecent(emoji: string) {
  const recent = getRecent().filter(e => e !== emoji);
  recent.unshift(emoji);
  localStorage.setItem(RECENT_KEY, JSON.stringify(recent.slice(0, 32)));
}

export function EmojiPicker({ onSelect, onClose }: Props) {
  const [activeCategory, setActiveCategory] = useState('smileys');
  const [search, setSearch] = useState('');
  const [recent, setRecent] = useState(getRecent);
  const ref = useRef<HTMLDivElement>(null);

  useEffect(() => {
    const handler = (e: MouseEvent) => {
      if (ref.current && !ref.current.contains(e.target as Node)) onClose();
    };
    document.addEventListener('mousedown', handler);
    return () => document.removeEventListener('mousedown', handler);
  }, [onClose]);

  const handleSelect = (emoji: string) => {
    addRecent(emoji);
    setRecent(getRecent());
    onSelect(emoji);
  };

  const currentEmojis = activeCategory === 'recent' ? recent : (EMOJI_DATA[activeCategory] || []);

  // Simple search: filter all emojis
  const filteredEmojis = search.trim()
    ? Object.values(EMOJI_DATA).flat().filter(e => e.includes(search))
    : currentEmojis;

  return (
    <div ref={ref} className="absolute bottom-full mb-2 left-0 w-[320px] bg-dark-700 border border-dark-500 rounded-xl shadow-xl z-50 flex flex-col" style={{ maxHeight: '350px' }}>
      {/* Search */}
      <div className="px-3 pt-3 pb-2">
        <input
          type="text"
          value={search}
          onChange={(e) => setSearch(e.target.value)}
          placeholder="Поиск..."
          className="w-full px-3 py-1.5 bg-dark-600 border border-dark-500 rounded-lg text-xs text-white placeholder-gray-500 focus:outline-none focus:border-accent"
          autoFocus
        />
      </div>

      {/* Category tabs */}
      {!search && (
        <div className="flex px-2 gap-0.5 border-b border-dark-500 pb-1">
          {CATEGORIES.map(cat => (
            <button
              key={cat.id}
              onClick={() => setActiveCategory(cat.id)}
              className={`flex-1 py-1 text-center text-sm rounded transition-colors ${
                activeCategory === cat.id ? 'bg-dark-500' : 'hover:bg-dark-600'
              }`}
              title={cat.label}
            >
              {cat.icon}
            </button>
          ))}
        </div>
      )}

      {/* Emoji grid */}
      <div className="flex-1 overflow-y-auto px-2 py-2">
        {filteredEmojis.length === 0 ? (
          <p className="text-xs text-gray-500 text-center py-4">
            {activeCategory === 'recent' ? 'Нет недавних' : 'Не найдено'}
          </p>
        ) : (
          <div className="grid grid-cols-8 gap-0.5">
            {filteredEmojis.map((emoji, i) => (
              <button
                key={`${emoji}-${i}`}
                onClick={() => handleSelect(emoji)}
                className="w-9 h-9 flex items-center justify-center text-xl hover:bg-dark-500 rounded transition-colors"
              >
                {emoji}
              </button>
            ))}
          </div>
        )}
      </div>
    </div>
  );
}
